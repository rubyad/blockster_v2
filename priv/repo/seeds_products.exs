Enum.each(products_data, fn data ->
  # Skip if product with this handle already exists
  case Repo.one(from p in Product, where: p.handle == ^data.handle) do
    nil ->
      hub_id = if data.hub_name, do: Map.get(hub_ids, data.hub_name)
      
      {:ok, product} = %Product{}
        |> Product.changeset(%{
          title: data.title,
          handle: data.handle,
          body_html: data.body_html,
          vendor: data.vendor,
          product_type: data.product_type,
          status: data.status,
          hub_id: hub_id
        })
        |> Repo.insert()

      # Images
      Enum.each(data.images, fn img ->
        %ProductImage{}
        |> ProductImage.changeset(Map.put(img, :product_id, product.id))
        |> Repo.insert!()
      end)

      # Variants
      Enum.each(data.variants, fn var ->
        %ProductVariant{}
        |> ProductVariant.changeset(Map.put(var, :product_id, product.id))
        |> Repo.insert!()
      end)

      # Config
      if data.config do
        %ProductConfig{}
        |> ProductConfig.changeset(Map.merge(data.config, %{product_id: product.id}))
        |> Repo.insert!()
      end

      # Category assignments (join table, no schema module)
      Enum.each(data.categories, fn cat_name ->
        if cat_id = Map.get(category_ids, cat_name) do
          Repo.query!("INSERT INTO product_category_assignments (id, product_id, category_id, inserted_at, updated_at) VALUES ($1, $2, $3, $4, $4)",
            [Ecto.UUID.bingenerate(), Ecto.UUID.dump!(product.id), Ecto.UUID.dump!(cat_id), NaiveDateTime.utc_now() |> NaiveDateTime.truncate(:second)])
        end
      end)

      IO.puts("  Created: #{data.title}")

    _existing ->
      IO.puts("  Skipped (exists): #{data.title}")
  end
end)

IO.puts("\nProduct seeding complete! #{Repo.aggregate(Product, :count)} products in database.")
