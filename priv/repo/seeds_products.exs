}

category_ids = Enum.reduce(categories, %{}, fn {name, slug}, acc ->
  case Repo.one(from c in ProductCategory, where: c.slug == ^slug) do
    nil ->
      {:ok, cat} = %ProductCategory{}
        |> ProductCategory.changeset(%{name: name, slug: slug})
        |> Repo.insert()
      IO.puts("  Created category: #{name}")
      Map.put(acc, name, cat.id)
    existing ->
      IO.puts("  Category exists: #{name}")
      Map.put(acc, name, existing.id)
  end
end)

